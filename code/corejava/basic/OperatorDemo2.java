public class OperatorDemo2
{
	public static void main(String ar[])
	{
		int a = 10;
		int b = 2;
		int c = 4;
		
		a+=c; // a = a + c;
		System.out.println(a); // 14
		
		a-=c; // a = a - c;
		System.out.println(a); // 10
			
		a/=b; // a = a / b;
		System.out.println(a); // 5

		a*=c;// a = a * 4;
		System.out.println(a); // 20		
		

	}
}	